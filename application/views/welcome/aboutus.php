            <center><h2>Indian Springs Lodge #48</h2></center>
            <p>In the small Southern Nevada town of Indian Springs, 45 minutes northwest of Las Vegas, we find Nevada's Lodge Number 48.</p>

       <p> Their dispensation was issued in July of 1966, and their charter was given in November of that same year.</p>

        <p>This Lodge has seen the typical "boom and bust" so common to small Nevada towns; the economy of Indian Springs was based on the nearby Nevada Test Site of the Department of Energy and on Indian Springs Air Force Base, which is an "Auxiliary Field" of Nellis Air Force Base, in support of the Nellis Air Force Base Bombing and Gunnery Range - home of the "Red Flag" and "Green Flag" exercise - the Air Force equivalent of the Navy's "Top Gun" (also headquartered in Nevada, at NAS Fallon, east of Reno). With the reductions in force at the Test Site due to cessation of atomic testing, and the conversion of Indian Springs Air Force Base to a privately-operated field by the Air Force and the loss of virtually all Air Force personnel stationed in Indian Springs, the population of the town has dropped drastically, and the Lodge is currently supported by members who drive in from Las Vegas and Pahrump - a drive of up to an hour and a half each way - for its meetings, along with a small core of Indian Springs residents.</p>

        <p>The Lodge is famous for its "Cornbread and Beans" dinners, served at most meetings - if you ever have the opportunity to attend and partake, I heartily recommend it!</p>

        <p>Their Lodge building (pictured below) was built with the sweat of their brows and the bending of their backs, and is a very solid, warm and friendly Lodge building.</p>

